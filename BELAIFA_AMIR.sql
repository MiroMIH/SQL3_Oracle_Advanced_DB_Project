CREATE OR REPLACE TYPE  BODY tsportif AS 
    MEMBER FUNCTION F_SPORTIF_ENTRAINER RETURN NUMERIC IS 
        F_SPORTIF_ENTRAINER_VAL NUMBER;
    BEGIN
        SELECT COUNT(deref(COLUMN_VALUE)) INTO F_SPORTIF_ENTRAINER_VAL
        FROM TABLE(SELECT sportif_entraineurs FROM sportif 
				WHERE idsportif = SELF.idsportif);
        RETURN F_SPORTIF_ENTRAINER_VAL;
    END;
END;
/

select v.F_SPORTIF_ENTRAINER () from sportif v;

CREATE OR REPLACE TYPE  BODY tsport AS 
    MEMBER FUNCTION F_GYM_NUMBER RETURN NUMERIC IS 
        F_GYM_NUMBER_VAL NUMBER;
    BEGIN
        SELECT COUNT(deref(COLUMN_VALUE)) INTO F_GYM_NUMBER_VAL
        FROM TABLE(SELECT sport_seance FROM sport WHERE idsport = SELF.idsport);
        RETURN F_GYM_NUMBER_VAL;
    END;
END;
/
select v.F_GYM_NUMBER () from sport v;
CREATE OR REPLACE TYPE  BODY tVille AS 
    MEMBER FUNCTION F_MOYEN_GYM RETURN NUMERIC IS 
        F_MOYEN_GYM_VAL NUMBER;
    BEGIN
        SELECT SUM(G.surface)/COUNT(G.idGymnase) INTO F_MOYEN_GYM_VAL 
        FROM GYMNASES G 
        WHERE DEREF(G.ville) = SELF;
        RETURN F_MOYEN_GYM_VAL;
    END;
END;
/
select v.F_MOYEN_GYM() from ville v;

